#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
    // 사용자가 입력하는 숫자를 저장하기 위한 배열이다.
    // 최대 100개의 정수를 저장할 수 있도록 선언하였다.
    int num[100];

    // 배열 이름을 포인터로 사용하여
    // 포인터 연산으로 배열에 접근하기 위해 선언하였다.
    int *p = num;

    // 현재까지 입력받은 숫자의 개수를 저장하는 변수이다.
    // 배열에 저장할 위치를 결정하는 용도로도 함께 사용하였다.
    int cnt = 0;

    // 사용자로부터 입력받은 값을 임시로 저장하는 변수이다.
    int input;

    // 사용자가 -1을 입력하기 전까지
    // 계속해서 숫자를 입력받기 위해 무한 반복문으로 구현하였다.
    while (1)
    {
        // 첫 번째 입력과 이후 입력의 안내 문구를 구분하여
        // 사용자가 현재 입력 순서를 쉽게 알 수 있도록 하였다.
        if (cnt == 0)
            printf("첫 번째 숫자 입력(-1 종료) : ");
        else
            printf("다음 숫자 입력(-1 종료) : ");

        // 사용자에게 정수를 입력받는다.
        scanf("%d", &input);

        // -1은 프로그램 종료를 위한 약속된 값이므로
        // 입력되면 반복문을 종료한다.
        if (input == -1)
            break;

        // 포인터 연산을 이용하여 현재 저장 위치에 입력값을 저장하였다.
        // 배열 인덱스를 사용하지 않고 포인터만으로 접근하도록 구현하였다.
        *(p + cnt) = input;

        // 하나의 숫자를 저장했으므로
        // 다음 저장 위치를 위해 개수를 증가시킨다.
        cnt++;

        printf("\n");

        // 현재까지 입력된 숫자를 이용하여
        // 총 12개의 숫자를 반복해서 출력한다.
        // 입력 개수가 부족한 경우에는 처음부터 다시 출력되도록
        // 나머지 연산(%)을 이용하였다.
        for (int i = 0; i < 12; i++)
        {
            // i % cnt를 이용하여
            // 입력된 숫자를 순환하면서 반복 출력한다.
            printf("%d", *(p + (i % cnt)));

            // 4개의 숫자를 출력할 때마다 줄을 바꾸어
            // 4×3 형태로 보기 쉽게 출력하였다.
            if ((i + 1) % 4 == 0)
                printf("\n");
        }

        // 다음 입력 결과와 구분하기 위해
        // 한 줄을 추가로 출력하였다.
        printf("\n");
    }

    // 프로그램을 정상적으로 종료한다.
    return 0;
}