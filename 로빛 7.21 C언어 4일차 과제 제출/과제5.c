#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>

int main()
{
    char str[100];

    // 각 문자가 몇 번 등장했는지 저장하기 위한 배열이다.
    int count[256] = {0};

    // 문자열을 처음부터 끝까지 탐색하기 위해 사용할 포인터이다.
    char *p;

    printf("입력 : ");

    // 공백을 포함한 한 줄 전체를 입력받기 위해 fgets()를 사용하였다.
    fgets(str, sizeof(str), stdin);

    // 문자열의 마지막 위치를 찾기 위해
    // 포인터를 한 칸씩 이동하면서 문자열의 끝('\0')을 탐색한다.
    p = str;
    while (*p != '\0')
        p++;

    // fgets()는 사용자가 Enter를 누르면 개행문자('\n')까지 함께 저장한다.
    // 문자열을 깔끔하게 사용하기 위해 마지막 개행문자를 제거하였다.
    if (*(p - 1) == '\n')
    {
        *(p - 1) = '\0';
        p--;
    }

    // 문자열을 처음부터 다시 탐색하면서
    // 각 문자가 등장한 횟수를 계산한다.
    p = str;
    while (*p != '\0')
    {
        // 공백은 문자 개수 계산에서 제외하도록 하였다.
        // 필요에 따라 이 조건을 제거하면 공백도 함께 계산할 수 있다.
        if (*p != ' ')
            count[(unsigned char)*p]++;

        // 다음 문자로 이동하여 같은 작업을 반복한다.
        p++;
    }

    // 문자열을 거꾸로 출력하기 위해
    // 현재 포인터를 마지막 문자 위치로 한 칸 이동시킨다.
    printf("출력 : ");
    p--;

    // 마지막 문자부터 첫 번째 문자까지 역순으로 이동하면서 출력한다.
    while (p >= str)
    {
        printf("%c", *p);
        p--;
    }

    // 가장 많이 등장한 문자와 등장 횟수를 저장하기 위한 변수이다.
    char maxchar = '\0';
    int max = 0;

    // 문자 개수를 저장한 배열을 처음부터 끝까지 확인하면서
    // 가장 큰 값을 가지는 문자를 찾는다.
    for (int i = 0; i < 256; i++)
    {
        // 현재 문자의 등장 횟수가 지금까지의 최대값보다 크다면
        // 최대 등장 횟수와 해당 문자를 새롭게 저장한다.
        if (count[i] > max)
        {
            max = count[i];
            maxchar = i;
        }
    }

    // 가장 많이 등장한 문자를 출력한다.
    printf("\n최다등장문자 : %c\n", maxchar);

    // 프로그램을 정상적으로 종료한다.
    return 0;
}